import turtle
jack = turtle.Turtle()
jack.color("yellow")
for side in range(4):
    if side == 1:
        jack.color("blue")
    if side == 2:
        jack.color("red")
    if side == 3:
        jack.color("cyan")
    if side == 4:
        jack.color("magenta")
    jack.forward(100)
    jack.right(90)



    
